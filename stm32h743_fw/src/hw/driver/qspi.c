bool qspiInit(void)
{
  bool ret = true;
  QSPI_Info info;


  if (BSP_QSPI_Init() == QSPI_OK)
  {
    ret = true;
  }
  else
  {
    ret = false;
  }


  if (BSP_QSPI_GetID(&info) == QSPI_OK)
  {
    if (info.device_id[0] == 0xEF && info.device_id[1] == 0x40 && info.device_id[2] == 0x17)
    {
      logPrintf("W25Q64JV(QSPI)    \t: OK\r\n");
      ret = true;
    }
    else
    {
      logPrintf("W25Q64JV(QSPI)    \t : Fail %X %X %X\r\n", info.device_id[0], info.device_id[1], info.device_id[2]);
      ret = false;
    }
  }
  else
  {
    logPrintf("QSPI                \t : Fail\r\n");
    ret = false;
  }



  is_init = ret;

  return ret;
}

